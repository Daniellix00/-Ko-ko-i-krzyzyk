package com.kodilla.mechanika;
public class RuchKomputera {
    private final int wiersz;
    private final int kolumna;

    public RuchKomputera(int wiersz, int kolumna) {
        this.wiersz = wiersz;
        this.kolumna = kolumna;
    }
    public int getWiersz() {
        return wiersz;
    }
    public int getKolumna() {
        return kolumna;
    }
}


